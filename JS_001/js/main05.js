// 우리나라만세 라는 변수를 정의(define) 하고
// Republic of Korea 라는 문자열을 보관(Setting) 한다
const 우리나라만세 = "Republic of Korea";
// 우리나라만세 라는 변수에 보관된 내용을 화면에 write 하라
document.write(우리나라만세);

/**
 * 대한민국 이라는 변수가 아직 define 되지 않았는데
 * 변수에 보관된 내용을 화면에 write 하라 라고 했기 때문에
 * 오류가 발생한다
 *
 * 다음 라인 코드에서 대한민국 변수를 define 하였지만
 * 코드의 진행 순서상 뒤에 나오기 때문에 의미가 없다
 */
// 대한민국 이라는 변수에 보관된 내용을 화면에 write 하라
document.write(대한민국);
// 대한민국 이라는 변수를 정의(define) 하고
// 우리나라 라는 문자열을 보관한다
const 대한민국 = "우리나라";
